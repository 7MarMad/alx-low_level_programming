#include <stdio.h>

/**
 * op_add - adding two numbers
 * @a: 1st number
 * @b: 2nd number
 *
 * Return: sum of the two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subbstruction of two numbers
 * @a: 1st number
 * @b: 2nd number
 *
 * Return: result of sub of 2 numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication of 2 numbers
 * @a: 1st number
 * @b: 2nd number
 *
 * Return: result of mul of 2 numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of 2 numbers
 * @a: 1st number
 * @b: 2nd number
 *
 * Return: result of division of 2 numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modul of 2 numbers
 * @a: 1st number
 * @b: 2nd number
 *
 * Return: result of modulo of 2 numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
