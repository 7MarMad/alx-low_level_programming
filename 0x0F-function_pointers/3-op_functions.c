/**
 * op_add - addition of two num
 * @a: arg
 * @b: arg2
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction of two num
 * @a: arg
 * @b: arg2
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul of two numb
 * @a: arg
 * @b: arg2
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division of two num
 * @a: arg
 * @b: arg2
 * Return: Result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo of two num
 * @a: arg
 * @b: arg2
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
